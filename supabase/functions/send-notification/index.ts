function random(arr: string[]) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function getTimeOfDay(hour: number) {
  if (hour >= 6 && hour < 12) return 'Доброе утро';
  if (hour >= 12 && hour < 18) return 'Добрый день';
  return 'Добрый вечер';
}

async function sendMessage(chatId: number, text: string, botToken: string) {
  await fetch(`https://api.telegram.org/bot${botToken}/sendMessage`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ chat_id: chatId, text, parse_mode: 'HTML' }),
  });
}

function getBotToken(selectedBot: string) {
  return selectedBot === 'tyler' ? BOT_TOKEN_2 : BOT_TOKEN_1;
}

function fill(phrase: string, reviewer: string, submitter: string, timeOfDay: string) {
  return phrase
    .replace(/\{ИМЯ_ПРОВЕРЯЮЩЕГО\}/g, reviewer)
    .replace(/\{ИМЯ_СДАВШЕГО\}/g, `<b>${submitter}</b>`)
    .replace(/\{ИМЯ\}/g, reviewer)
    .replace(/\{ВРЕМЯ_СУТОК\}/g, timeOfDay);
}

function fillSimple(phrase: string, name: string, timeOfDay: string) {
  return phrase.replace(/\{ИМЯ\}/g, name).replace(/\{ВРЕМЯ_СУТОК\}/g, timeOfDay);
}

function fillPrize(phrase: string, reviewer: string, buyer: string, prize: string, timeOfDay: string) {
  return phrase
    .replace(/\{ИМЯ_ПРОВЕРЯЮЩЕГО\}/g, reviewer)
    .replace(/\{ИМЯ_СДАВШЕГО\}/g, `<b>${buyer}</b>`)
    .replace(/\{ПРИЗ\}/g, `<b>${prize}</b>`)
    .replace(/\{ВРЕМЯ_СУТОК\}/g, timeOfDay);
}

