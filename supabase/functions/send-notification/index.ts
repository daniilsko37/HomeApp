Deno.serve(async (req) => {
  let body: any = {};
  try {
    const text = await req.text();
    if (text) body = JSON.parse(text);
  } catch {
    body = {};
  }

  const now = new Date();
  const moscowTime = new Date(now.getTime() + 3 * 60 * 60000);
  const moscowHour = moscowTime.getUTCHours();
  const currentTime = `${String(moscowHour).padStart(2, '0')}:${String(moscowTime.getUTCMinutes()).padStart(2, '0')}`;
  const timeOfDay = getTimeOfDay(moscowHour);
  const todayMoscow = moscowTime.toISOString().split('T')[0];

  // ── Мгновенное уведомление о покупке приза ─────────────────────────────
  if (body.type === 'prize') {
    const { room_id, buyer_name, item_title } = body;

    const { data: profiles } = await supabase
      .from('profiles')
      .select('name, gender, telegram_chat_id, selected_bot')
      .eq('room_id', room_id);

    if (!profiles) return new Response('ok', { status: 200 });

    const buyer = profiles.find((p: any) => p.name === buyer_name);
    const others = profiles.filter((p: any) => p.name !== buyer_name && p.telegram_chat_id);
    if (!buyer) return new Response('ok', { status: 200 });

    const buyerIsMale = buyer.gender !== 'female';

    for (const other of others) {
      const botToken = getBotToken(other.selected_bot);
      const receiverIsMale = other.gender !== 'female';
      const isTyler = other.selected_bot === 'tyler';

      let phrase: string;
      if (isTyler) {
        phrase = fillPrize(random(tyler_prize), other.name, buyer_name, item_title, timeOfDay);
        await sendMessage(other.telegram_chat_id, `🧨 Новая покупка\n\n${phrase}`, botToken);
      } else {
        const phrases = receiverIsMale && !buyerIsMale
          ? sirBlesk_prizeMaleReceiverFemaleBuyer
          : !receiverIsMale && buyerIsMale
          ? sirBlesk_prizeFemaleReceiverMaleBuyer
          : receiverIsMale ? sirBlesk_prizeMaleReceiverFemaleBuyer : sirBlesk_prizeFemaleReceiverMaleBuyer;
        phrase = fillPrize(random(phrases), other.name, buyer_name, item_title, timeOfDay);
        await sendMessage(other.telegram_chat_id, `🎁 Новая покупка!\n\n${phrase}`, botToken);
      }
    }

    return new Response('ok', { status: 200 });
  }

  // ── Плановые уведомления ───────────────────────────────────────────────
  const { data: users } = await supabase
    .from('profiles')
    .select('id, name, gender, telegram_chat_id, selected_bot, notif_new_enabled, notif_new_time, notif_overdue_enabled, notif_overdue_time, notif_check_enabled, notif_check_time, room_id')
    .not('telegram_chat_id', 'is', null);

  if (!users) return new Response('ok', { status: 200 });

  for (const user of users) {
    const isMale = user.gender !== 'female';
    const name = user.name;
    const isTyler = user.selected_bot === 'tyler';
    const botToken = getBotToken(user.selected_bot);

    // ── Новые задачи ──────────────────────────────────────────────────────
    if (user.notif_new_enabled && user.notif_new_time?.slice(0, 5) === currentTime) {
      const { data: tasks } = await supabase
        .from('tasks')
        .select('title')
        .eq('room_id', user.room_id)
        .eq('status', 'todo');

      const count = tasks?.length ?? 0;
      if (count > 0) {
        const taskList = tasks!.map((t: any) => `• ${t.title}`).join('\n');

        if (isTyler) {
          const greeting = fillSimple(random(tyler_greetings1), name, timeOfDay);
          const message = `${greeting}\n\n${taskList}`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        } else {
          const greeting = fillSimple(random(isMale ? sirBlesk_greetingsMale : sirBlesk_greetingsFemale), name, timeOfDay);
          const closing = fillSimple(random(isMale ? sirBlesk_closingsMale : sirBlesk_closingsFemale), name, timeOfDay);
          const message = `${greeting}\n\n${taskList}\n\n${closing}`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        }
      }
    }

    // ── Просрочка ─────────────────────────────────────────────────────────
    if (user.notif_overdue_enabled && user.notif_overdue_time?.slice(0, 5) === currentTime) {
      const { data: allTasks } = await supabase
        .from('tasks')
        .select('title, status, assignee, start_date, last_penalty_date, type')
        .eq('room_id', user.room_id)
        .in('status', ['todo', 'in_progress'])
        .in('type', ['green', 'blue']);

      if (!allTasks) continue;

      const moscowToday = new Date(moscowTime);
      moscowToday.setUTCHours(0, 0, 0, 0);

      const overdueTasks = allTasks.filter((task: any) => {
        if (!task.start_date) return false;
        const start = new Date(task.start_date);
        start.setHours(0, 0, 0, 0);
        const diffDays = Math.floor((moscowToday.getTime() - start.getTime()) / (1000 * 60 * 60 * 24));
        if (diffDays < 3) return false;
        if (task.last_penalty_date === todayMoscow) return false;
        if (task.status === 'todo') return true;
        if (task.status === 'in_progress' && task.assignee === user.name) return true;
        return false;
      });

      if (overdueTasks.length > 0) {
        const taskList = overdueTasks.map((t: any) => `• ${t.title}`).join('\n');

        if (isTyler) {
          const phrase = random(tyler_overdue);
          const message = `${phrase}\n\n🔴 Просрочено:\n\n${taskList}\n\n💸 Штраф: -1 балл за каждую задачу. Ежедневно.`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        } else {
          const phrase = random(sirBlesk_overdue);
          const message = `${name}, ${phrase}\n\n⚠️ Обратите внимание на просроченные задачи:\n\n${taskList}`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        }
      }
    }

    // ── Проверка партнёра ─────────────────────────────────────────────────
    if (user.notif_check_enabled && user.notif_check_time?.slice(0, 5) === currentTime) {
      const { data: pendingTasks } = await supabase
        .from('tasks')
        .select('title, assignee')
        .eq('room_id', user.room_id)
        .eq('status', 'pending_approval')
        .neq('assignee', user.name);

      if (!pendingTasks || pendingTasks.length === 0) continue;

      const { data: roomProfiles } = await supabase
        .from('profiles')
        .select('name, gender')
        .eq('room_id', user.room_id);

      const byAssignee: Record<string, string[]> = {};
      for (const task of pendingTasks) {
        if (!task.assignee) continue;
        if (!byAssignee[task.assignee]) byAssignee[task.assignee] = [];
        byAssignee[task.assignee].push(task.title);
      }

      for (const [submitterName, titles] of Object.entries(byAssignee)) {
        const submitterProfile = roomProfiles?.find((p: any) => p.name === submitterName);
        const submitterIsMale = submitterProfile?.gender !== 'female';
        const taskList = titles.map((t: string) => `• ${t}`).join('\n');

        if (isTyler) {
          const phrase = fill(random(tyler_review), name, submitterName, timeOfDay);
          const message = `💢 Требуется проверка\n\n${phrase}\n\n${taskList}`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        } else {
          let phrases;
          if (isMale && !submitterIsMale) phrases = sirBlesk_reviewFemaleReviewerMaleSub;
          else if (!isMale && submitterIsMale) phrases = sirBlesk_reviewMaleReviewerFemaleSub;
          else phrases = isMale ? sirBlesk_reviewFemaleReviewerMaleSub : sirBlesk_reviewMaleReviewerFemaleSub;

          const phrase = fill(random(phrases), name, submitterName, timeOfDay);
          const message = `${phrase}\n\n🔍 На вашу проверку поданы:\n\n${taskList}`;
          await sendMessage(user.telegram_chat_id, message, botToken);
        }
      }
    }
  }

  return new Response('ok', { status: 200 });
});
